  FREE_SHARES_RATIO DOUBLE,
  TOTAL_SHARES_RATIO DOUBLE,
  INDEX_CLOSE DOUBLE,
  PRE_CLOSE_PRICE DOUBLE,
  TOTAL_SHARES BIGINT,
  DISCOUNT_RATIO DOUBLE,
  PRIMARY KEY (SECUCODE, TRADE_DATE)
