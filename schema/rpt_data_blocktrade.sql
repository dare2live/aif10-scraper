  SECURITY_NAME_ABBR VARCHAR NOT NULL,
  SECURITY_TYPE VARCHAR NOT NULL,
  TRADE_DATE TIMESTAMP NOT NULL,
  DEAL_PRICE DOUBLE NOT NULL,
  PREMIUM_RATIO DOUBLE NOT NULL,
  DEAL_VOLUME INTEGER NOT NULL,
  DEAL_AMT DOUBLE NOT NULL,
  BUYER_NAME VARCHAR NOT NULL,
  SELLER_NAME VARCHAR NOT NULL,
  SECURITY_TYPE_CODE VARCHAR NOT NULL,
  DAILY_RANK INTEGER NOT NULL,
  CLOSE_PRICE DOUBLE NOT NULL,
  TRADE_UNIT VARCHAR NOT NULL,
  TURNOVER_RATE DOUBLE,
